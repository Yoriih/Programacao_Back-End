# Exercício 4: Cálculo de Área de um Retângulo

# Solicite a largura e a altura de um retângulo

largura = float(input("Qual a largura em cm do Retângulo? "))
altura = float(input("Qual a altura em cm do Retângulo? "))

# E calcule a Área do triângulo = (largura x altura) / 2

area = (largura * altura) / 2

#Resultado

print("A area do retângulo é: ",area)
