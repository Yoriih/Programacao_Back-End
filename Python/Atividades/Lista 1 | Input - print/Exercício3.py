# Exercício 3: Conversão de Temperatura

# Peça uma temperatura em Celsius ao usuário

Celsius = int(input("Qual a temperatura em Celsius? "))

# Converta-a para Fahrenheit utilizando a fórmula: Fahrenheit = (Celsius * 9/5) + 32.

Fahrenheit = (Celsius * 9/5) + 32

#Resultado

print("A temperatura em Far=hrenheit é:  ",Fahrenheit)