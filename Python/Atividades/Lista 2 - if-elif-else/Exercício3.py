# Exercício 3: Verificação de Idade para Dirigir

# Peça a idade do usuário 

idade = int(input("DIGITE SUA IDADE: "))

# Verifique se ele tem idade suficiente para dirigir (idade mínima de 18 anos).

if idade >= 18:
    print("VOCÊ TEM IDADE PARA DIRIGIR")
else:

    print("VOCÊ NÃO TEM IDADE SUFICIENTE PARA DIRIGIR")
    
