# Exercício 1: Contagem Crescente

# Crie um loop que conte de 1 a 10 

contador = 1

# Mostre cada número.
# While significa enquanto, ou seja, enquanto a condição for verdadeira o loop continua.
# O loop só para quando a condição for falsa.
# No caso, enquanto contato for menor ou igual a 10, o loop continua.

while contador <= 10:
    print(contador)
    contador = contador + 1

