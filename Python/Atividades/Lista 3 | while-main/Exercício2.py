# Exercício 2: Contagem Regressiva

# Crie um loop que conte de 10 a 1

contador = 10                                   # Inicializa o contador em 10

# Mostre cada número.

while contador > 0:                             # Enquanto o contador for maior que 0
    print(contador)                             # Mostra o número atual
    contador = contador - 1                     # Decrementa o contador em 1
